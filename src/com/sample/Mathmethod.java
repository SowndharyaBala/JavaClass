package com.sample;

public class Mathmethod {

	public static void main(String[] args) {
		
		System.out.println(Math.max(14,5));
		System.out.println(Math.min(14,5));
		System.out.println(Math.round(5.495));
		System.out.println(Math.sqrt(89));
		System.out.println(Math.pow(3,2));
		System.out.println(Math.ceil(14.1));
		System.out.println(Math.floor(14.78));
		System.out.println(Math.round(14.2));
		System.out.println(Math.round(14.85));
		System.out.println(Math.random());
		System.out.println(Math.abs(-78));
	}

}
