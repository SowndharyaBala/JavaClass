package com.sample;

public class ForLoop {
	public static void main(String[] args) {
        int n =8; // Number of rows
        
        for (int i = 1; i<= n; i++) {
            for (int j =1 ; j<= i; j++) {
                System.out.print("*");
            }
            System.out.println(); // Move to the next line
        }
	
    }
	
}	
	







//*    row= col
//**  row=col
